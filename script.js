function update () {
    stats.begin();

    const dt = calcDeltaTime();
    updateColors(dt);
    applyInputs();
    if (!config.PAUSED)
        step(dt);
    render(null);

    opc.send();

    stats.end();
    requestAnimationFrame(update);
}

function calcDeltaTime () {
    let now = Date.now();
    let dt = (now - lastUpdateTime) / 1000;
    dt = Math.min(dt, 0.016666);
    lastUpdateTime = now;
    return dt;
}

function resizeCanvas(forceInit) {
  let width = window.innerWidth;
  let height = window.innerHeight;
  if (width / height > config.ASPECT_RATIO) {
    width = Math.round(height * config.ASPECT_RATIO);
  } else {
    height = Math.round(width / config.ASPECT_RATIO);
  }

  let guiLoc = gui.domElement.getBoundingClientRect();
  if (width > guiLoc.x || window.innerWidth / 2 + width / 2 <= guiLoc.x) {
    // GUI needs to overlap canvas or canvas will fit centered without overlap
    xOffset = Math.round(window.innerWidth / 2 - width / 2);
  } else {
    // Canvas will fit off-center without overlapping GUI
    xOffset = Math.round(guiLoc.x / 2 - width / 2);
  }

  yOffset = Math.round(window.innerHeight / 2 - height / 2);

  canvas.style.left = xOffset + 'px';
  canvas.style.top = yOffset + 'px';
  overlayCanvas.style.left = xOffset + 'px';
  overlayCanvas.style.top = yOffset + 'px';

  // Make sure GUI is on top
  gui.domElement.style.zIndex = 100;

  width += 'px';
  height += 'px';
  canvas.style.width = width;
  canvas.style.height = height;
  overlayCanvas.style.width = width;
  overlayCanvas.style.height = height;

  // Now make sure canvas logical size is correct
  width = scaleByPixelRatio(canvas.clientWidth);
  height = scaleByPixelRatio(canvas.clientHeight);
  if (forceInit || canvas.width != width || canvas.height != height) {
      canvas.width = width;
      canvas.height = height;
      opc.resize();
      initFramebuffers();
  }
}

function updateColors (dt) {
    if (!config.RAINBOW || !config.COLORFUL) return;

    colorUpdateTimer += dt * config.COLOR_UPDATE_SPEED;
    if (colorUpdateTimer >= 1) {
        colorUpdateTimer = wrap(colorUpdateTimer, 0, 1);
        touches.forEach(p => {
            if (p.local) {
                p.color = generateColor();
                if (realTimeActivity) {
                    realTimeActivity.setState({
                        ['c' + p.id]: p.color
                    });
                }
            }
        });
    }
}

function applyInputs () {
    touches.forEach(p => {
        // "complete" boolean shortcuts all these other tests to save time?
        if (!p.complete && (!p.pos || !p.color || !p.radius || !p.force)) {
          return;
        }
        // If we got this far, it's complete, so set the flag.
        p.complete = true;
        let curPos = null, prevPos = p.pos;
        while (p.history.length) {
            curPos = prevPos;
            prevPos = p.history.pop();
            splat(curPos, prevPos, p);
        }
        if (!curPos) {
          splat(p.pos, p.pos, p);
        }
    });
}

function step (dt) {
    gl.disable(gl.BLEND);
    gl.viewport(0, 0, velocity.width, velocity.height);

    curlProgram.bind();
    gl.uniform2f(curlProgram.uniforms.texelSize, velocity.texelSizeX, velocity.texelSizeY);
    gl.uniform1i(curlProgram.uniforms.uVelocity, velocity.read.attach(0));
    blit(curl.fbo);

    vorticityProgram.bind();
    gl.uniform2f(vorticityProgram.uniforms.texelSize, velocity.texelSizeX, velocity.texelSizeY);
    gl.uniform1i(vorticityProgram.uniforms.uVelocity, velocity.read.attach(0));
    gl.uniform1i(vorticityProgram.uniforms.uCurl, curl.attach(1));
    gl.uniform1f(vorticityProgram.uniforms.curl, config.CURL);
    gl.uniform1f(vorticityProgram.uniforms.dt, dt);
    blit(velocity.write.fbo);
    velocity.swap();

    divergenceProgram.bind();
    gl.uniform2f(divergenceProgram.uniforms.texelSize, velocity.texelSizeX, velocity.texelSizeY);
    gl.uniform1i(divergenceProgram.uniforms.uVelocity, velocity.read.attach(0));
    blit(divergence.fbo);

    clearProgram.bind();
    gl.uniform1i(clearProgram.uniforms.uTexture, pressure.read.attach(0));
    gl.uniform1f(clearProgram.uniforms.value, config.PRESSURE);
    blit(pressure.write.fbo);
    pressure.swap();

    pressureProgram.bind();
    gl.uniform2f(pressureProgram.uniforms.texelSize, velocity.texelSizeX, velocity.texelSizeY);
    gl.uniform1i(pressureProgram.uniforms.uDivergence, divergence.attach(0));
    for (let i = 0; i < config.PRESSURE_ITERATIONS; i++) {
        gl.uniform1i(pressureProgram.uniforms.uPressure, pressure.read.attach(1));
        blit(pressure.write.fbo);
        pressure.swap();
    }

    gradienSubtractProgram.bind();
    gl.uniform2f(gradienSubtractProgram.uniforms.texelSize, velocity.texelSizeX, velocity.texelSizeY);
    gl.uniform1i(gradienSubtractProgram.uniforms.uPressure, pressure.read.attach(0));
    gl.uniform1i(gradienSubtractProgram.uniforms.uVelocity, velocity.read.attach(1));
    blit(velocity.write.fbo);
    velocity.swap();

    advectionProgram.bind();
    gl.uniform2f(advectionProgram.uniforms.texelSize, velocity.texelSizeX, velocity.texelSizeY);
    if (!ext.supportLinearFiltering)
        gl.uniform2f(advectionProgram.uniforms.dyeTexelSize, velocity.texelSizeX, velocity.texelSizeY);
    let velocityId = velocity.read.attach(0);
    gl.uniform1i(advectionProgram.uniforms.uVelocity, velocityId);
    gl.uniform1i(advectionProgram.uniforms.uSource, velocityId);
    gl.uniform1f(advectionProgram.uniforms.dt, dt);
    gl.uniform1f(advectionProgram.uniforms.dissipation, config.VELOCITY_DISSIPATION);
    blit(velocity.write.fbo);
    velocity.swap();

    gl.viewport(0, 0, dye.width, dye.height);

    if (!ext.supportLinearFiltering)
        gl.uniform2f(advectionProgram.uniforms.dyeTexelSize, dye.texelSizeX, dye.texelSizeY);
    gl.uniform1i(advectionProgram.uniforms.uVelocity, velocity.read.attach(0));
    gl.uniform1i(advectionProgram.uniforms.uSource, dye.read.attach(1));
    gl.uniform1f(advectionProgram.uniforms.dissipation, config.DENSITY_DISSIPATION);
    blit(dye.write.fbo);
    dye.swap();
}

function render (target) {
    if (config.BLOOM)
        applyBloom(dye.read, bloom);
    if (config.SUNRAYS) {
        applySunrays(dye.read, dye.write, sunrays);
        blur(sunrays, sunraysTemp, 1);
    }

    if (target == null || !config.TRANSPARENT) {
        gl.blendFunc(gl.ONE, gl.ONE_MINUS_SRC_ALPHA);
        gl.enable(gl.BLEND);
    }
    else {
        gl.disable(gl.BLEND);
    }

    let width = target == null ? gl.drawingBufferWidth : target.width;
    let height = target == null ? gl.drawingBufferHeight : target.height;
    gl.viewport(0, 0, width, height);

    let fbo = target == null ? null : target.fbo;
    if (!config.TRANSPARENT)
        drawColor(fbo, normalizeColor(config.BACK_COLOR));
    if (target == null && config.TRANSPARENT)
        drawCheckerboard(fbo);
    drawDisplay(fbo, width, height);
}

function drawColor (fbo, color) {
    colorProgram.bind();
    gl.uniform4f(colorProgram.uniforms.color, color.r, color.g, color.b, 1);
    blit(fbo);
}

function drawCheckerboard (fbo) {
    checkerboardProgram.bind();
    gl.uniform1f(checkerboardProgram.uniforms.aspectRatio, canvas.width / canvas.height);
    blit(fbo);
}

function drawDisplay (fbo, width, height) {
    displayMaterial.bind();
    if (config.SHADING)
        gl.uniform2f(displayMaterial.uniforms.texelSize, 1.0 / width, 1.0 / height);
    gl.uniform1i(displayMaterial.uniforms.uTexture, dye.read.attach(0));
    if (config.BLOOM) {
        gl.uniform1i(displayMaterial.uniforms.uBloom, bloom.attach(1));
        gl.uniform1i(displayMaterial.uniforms.uDithering, ditheringTexture.attach(2));
        let scale = getTextureScale(ditheringTexture, width, height);
        gl.uniform2f(displayMaterial.uniforms.ditherScale, scale.x, scale.y);
    }
    if (config.SUNRAYS)
        gl.uniform1i(displayMaterial.uniforms.uSunrays, sunrays.attach(3));
    blit(fbo);
}

function applyBloom (source, destination) {
    if (bloomFramebuffers.length < 2)
        return;

    let last = destination;

    gl.disable(gl.BLEND);
    bloomPrefilterProgram.bind();
    let knee = config.BLOOM_THRESHOLD * config.BLOOM_SOFT_KNEE + 0.0001;
    let curve0 = config.BLOOM_THRESHOLD - knee;
    let curve1 = knee * 2;
    let curve2 = 0.25 / knee;
    gl.uniform3f(bloomPrefilterProgram.uniforms.curve, curve0, curve1, curve2);
    gl.uniform1f(bloomPrefilterProgram.uniforms.threshold, config.BLOOM_THRESHOLD);
    gl.uniform1i(bloomPrefilterProgram.uniforms.uTexture, source.attach(0));
    gl.viewport(0, 0, last.width, last.height);
    blit(last.fbo);

    bloomBlurProgram.bind();
    for (let i = 0; i < bloomFramebuffers.length; i++) {
        let dest = bloomFramebuffers[i];
        gl.uniform2f(bloomBlurProgram.uniforms.texelSize, last.texelSizeX, last.texelSizeY);
        gl.uniform1i(bloomBlurProgram.uniforms.uTexture, last.attach(0));
        gl.viewport(0, 0, dest.width, dest.height);
        blit(dest.fbo);
        last = dest;
    }

    gl.blendFunc(gl.ONE, gl.ONE);
    gl.enable(gl.BLEND);

    for (let i = bloomFramebuffers.length - 2; i >= 0; i--) {
        let baseTex = bloomFramebuffers[i];
        gl.uniform2f(bloomBlurProgram.uniforms.texelSize, last.texelSizeX, last.texelSizeY);
        gl.uniform1i(bloomBlurProgram.uniforms.uTexture, last.attach(0));
        gl.viewport(0, 0, baseTex.width, baseTex.height);
        blit(baseTex.fbo);
        last = baseTex;
    }

    gl.disable(gl.BLEND);
    bloomFinalProgram.bind();
    gl.uniform2f(bloomFinalProgram.uniforms.texelSize, last.texelSizeX, last.texelSizeY);
    gl.uniform1i(bloomFinalProgram.uniforms.uTexture, last.attach(0));
    gl.uniform1f(bloomFinalProgram.uniforms.intensity, config.BLOOM_INTENSITY);
    gl.viewport(0, 0, destination.width, destination.height);
    blit(destination.fbo);
}

function applySunrays (source, mask, destination) {
    gl.disable(gl.BLEND);
    sunraysMaskProgram.bind();
    gl.uniform1i(sunraysMaskProgram.uniforms.uTexture, source.attach(0));
    gl.viewport(0, 0, mask.width, mask.height);
    blit(mask.fbo);

    sunraysProgram.bind();
    gl.uniform1f(sunraysProgram.uniforms.weight, config.SUNRAYS_WEIGHT);
    gl.uniform1i(sunraysProgram.uniforms.uTexture, mask.attach(0));
    gl.viewport(0, 0, destination.width, destination.height);
    blit(destination.fbo);
}

function blur (target, temp, iterations) {
    blurProgram.bind();
    for (let i = 0; i < iterations; i++) {
        gl.uniform2f(blurProgram.uniforms.texelSize, target.texelSizeX, 0.0);
        gl.uniform1i(blurProgram.uniforms.uTexture, target.attach(0));
        blit(temp.fbo);

        gl.uniform2f(blurProgram.uniforms.texelSize, 0.0, target.texelSizeY);
        gl.uniform1i(blurProgram.uniforms.uTexture, temp.attach(0));
        blit(target.fbo);
    }
}

function splat(pos, previous, touch) {
    gl.viewport(0, 0, velocity.width, velocity.height);
    splatProgram.bind();
    gl.uniform1i(splatProgram.uniforms.uTarget, velocity.read.attach(0));
    gl.uniform1f(splatProgram.uniforms.aspectRatio, canvas.width / canvas.height);
    gl.uniform2f(splatProgram.uniforms.point, pos.x, pos.y);
    gl.uniform2f(splatProgram.uniforms.previous, previous.x, previous.y);
    gl.uniform3f(splatProgram.uniforms.color, (pos.x-previous.x) * touch.force, (pos.y-previous.y) * touch.force, 0.0);
    gl.uniform1f(splatProgram.uniforms.radius, correctRadius(touch.radius / 100.0));
    blit(velocity.write.fbo);
    velocity.swap();

    gl.viewport(0, 0, dye.width, dye.height);
    gl.uniform1i(splatProgram.uniforms.uTarget, dye.read.attach(0));
    gl.uniform3f(splatProgram.uniforms.color, touch.color.r, touch.color.g, touch.color.b);
    blit(dye.write.fbo);
    dye.swap();
}

function correctRadius (radius) {
    let aspectRatio = canvas.width / canvas.height;
    if (aspectRatio > 1)
        radius *= aspectRatio;
    return radius;
}

function touchstart(id, x, y) {
  let touch = {
    id: id,
    // Leave unset for non-local touches
    local: true,
    // Non-local touches come in pieces, so we don't know if they're complete,
    // but we know this touch has all the needed properties.
    complete: true,
    pos: { x: scaleByPixelRatio(x) / canvas.width,
           y: 1.0 - scaleByPixelRatio(y) / canvas.height},
    color: generateColor(),
    radius: config.SPLAT_RADIUS,
    force: config.SPLAT_FORCE
  };
  if (realTimeActivity) {
    realTimeActivity.setState({
      ['p' + id]: touch.pos,
      ['c' + id]: touch.color,
      ['r' + id]: touch.radius,
      ['f' + id]: touch.force
    });
  }
  // Add history only after sending to remote listeners; they don't need it
  touch.history = [touch.pos];
  touches.push(touch);
}

function touchmove(id, x, y) {
  let touch = touches.find(p => p.id == id);
  if (!touch) {
    return;
  }
  x = scaleByPixelRatio(x) / canvas.width;
  y = 1.0 - scaleByPixelRatio(y) / canvas.height;
  touch.history.push({x: touch.pos.x, y: touch.pos.y});
  touch.pos.x = x;
  touch.pos.y = y;
  if (realTimeActivity) {
    realTimeActivity.setState({
      ['p' + id]: touch.pos
    });
  }
}

function touchend(id) {
  let idx = touches.findIndex(p => p && p.id == id);
  touches.splice(idx, 1);
  if (realTimeActivity) {
    realTimeActivity.removeState(['c' + id, 'p' + id, 'f' + id, 'r' + id]);
  }
}

canvas.addEventListener('mousedown', e => {
  touchstart('m', e.offsetX, e.offsetY);
});

canvas.addEventListener('mousemove', e => {
  touchmove('m', e.offsetX, e.offsetY);
});

window.addEventListener('mouseup', () => {
  touchend('m');
});

canvas.addEventListener('touchstart', e => {
    e.preventDefault();
    for (const touch of e.changedTouches) {
      touchstart(
        touch.identifier, touch.pageX - xOffset, touch.pageY - yOffset);
    };
});

canvas.addEventListener('touchmove', e => {
    e.preventDefault();
    for (const touch of e.changedTouches) {
      touchmove(
        touch.identifier, touch.pageX - xOffset, touch.pageY - yOffset);
    };
}, false);

window.addEventListener('touchend', e => {
    // Don't preventDefault() here; we need touchend events to bubble up
    // to our GUI to make mobile clicks register properly
    for (const touch of e.changedTouches) {
      touchend(touch.identifier);
    };
});

function realTimeActivityChange(evt) {
  // XXX: Local is true for different browsers/windows on same machine??
  if (evt.sessionId == sessionId) {
    return;
  }
  let id = evt.sessionId + evt.oldValues.keys().next().value.substring(1);
  let idx = touches.findIndex(p => p.id == id);
  if (evt.removed.length) {
    touches.splice(idx, 1);
  } else {
    let touch;
    if (idx == -1) {
      touch = { id: id, history: [] };
      touches.push(touch);
    } else {
      touch = touches[idx];
      touch.history.push(touch.pos);
    }
    evt.values.forEach((value, key) => {
      switch(key[0]) {
        case 'c':
          touch.color = value;
          break;
        case 'p':
          touch.pos = value;
          if (idx == -1) {
            touch.history.push(touch.pos);
          }
          break;
        case 'r':
          touch.radius = value;
          break;
        case 'f':
          touch.force = value;
          break;
      }
    });
  }
}

